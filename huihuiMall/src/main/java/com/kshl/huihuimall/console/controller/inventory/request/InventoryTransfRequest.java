package com.kshl.huihuimall.console.controller.inventory.request;

import com.kshl.huihuimall.base.dal.entity.InventoryTransfer;

import java.util.Date;

public class InventoryTransfRequest extends InventoryTransfer {
    private Date createTime;
    private String createUser;
    private String rkName;
    private String ckName;
    private String code;
    private String rkPeople;
    private String ckPeople;

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public String getCreateUser() {
        return createUser;
    }

    public void setCreateUser(String createUser) {
        this.createUser = createUser;
    }

    public String getRkName() {
        return rkName;
    }

    public void setRkName(String rkName) {
        this.rkName = rkName;
    }

    public String getCkName() {
        return ckName;
    }

    public void setCkName(String ckName) {
        this.ckName = ckName;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getRkPeople() {
        return rkPeople;
    }

    public void setRkPeople(String rkPeople) {
        this.rkPeople = rkPeople;
    }

    public String getCkPeople() {
        return ckPeople;
    }

    public void setCkPeople(String ckPeople) {
        this.ckPeople = ckPeople;
    }
}
